package gc.david.dfm.ui;

import android.app.Activity;
import android.content.Intent;
import android.os.Bundle;
import android.support.v7.app.AppCompatActivity;
import android.support.v7.widget.Toolbar;
import android.view.MenuItem;

import butterknife.BindView;
import butterknife.ButterKnife;
import gc.david.dfm.R;

/**
 * Created by david on 24.01.17.
 */
public class AboutActivity extends AppCompatActivity {

    @BindView(R.id.tbMain)
    protected Toolbar toolbar;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_about);
        ButterKnife.bind(this);

        setSupportActionBar(toolbar);
        getSupportActionBar().setDisplayHomeAsUpEnabled(true);

        if (savedInstanceState == null) {
            getSupportFragmentManager().beginTransaction()
                                       .add(R.id.about_activity_container_framelayout, new OpenSourceMasterFragment())
                                       .commit();
        }
    }

    public static void open(final Activity activity) {
        final Intent openAboutActivityIntent = new Intent(activity, AboutActivity.class);
        activity.startActivity(openAboutActivityIntent);
    }

    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        switch (item.getItemId()) {
            case android.R.id.home:
                onBackPressed();
                return true;
            default:
                return super.onOptionsItemSelected(item);
        }
    }
}
